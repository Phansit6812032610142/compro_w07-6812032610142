#include <stdio.h>

int main() {
    float score;  // ประกาศตัวแปร score ชนิด float สำหรับเก็บคะแนนที่มีทศนิยม

    printf("Enter your score: ");  // แสดงข้อความเพื่อให้ผู้ใช้ป้อนคะแนน
    scanf("%f", &score);           // รับค่าคะแนนจากผู้ใช้และเก็บไว้ในตัวแปร score

    if(score >= 50) {  // ตรวจสอบว่าคะแนนมากกว่าหรือเท่ากับ 50 หรือไม่
        printf("Pass\n");  // ถ้าคะแนนมากกว่าหรือเท่ากับ 50 ให้แสดงคำว่า "Pass"

        score = score + (score * (5.0 / 100));  // คำนวณคะแนนใหม่โดยเพิ่ม 5% ของคะแนนเดิม
        printf("Your final score is %.2f\n", score);  // แสดงคะแนนสุดท้ายหลังจากเพิ่ม 5%
    }
    else {  // ถ้าคะแนนน้อยกว่า 50
        printf("Your final score is %.2f\n", score);  // แสดงคะแนนที่ได้รับโดยไม่มีการเปลี่ยนแปลง
    }

    return 0;  // ส่งค่ากลับ 0 เพื่อแสดงว่าโปรแกรมสิ้นสุดการทำงานอย่างถูกต้อง
}
